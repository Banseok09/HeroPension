DROP TABLE REVIEWBBS  CASCADE CONSTRAINTS;
DROP SEQUENCE SEQ_REVIEW;


CREATE TABLE REVIEWBBS(
	REVIEW_SEQ NUMBER(8),
	ID VARCHAR2(20) NOT NULL, 
	TITLE VARCHAR2(40) NOT NULL,
	CONTENT VARCHAR2(400) NOT NULL,
	RATE NUMBER(8) NOT NULL,
	REF NUMBER(8) NOT NULL,
	STEP NUMBER(8) NOT NULL,
	DEPTH NUMBER(8) NOT NULL,
	ROOM_SEQ NUMBER(8) NOT NULL,
	WDATE DATE NOT NULL,
	DEL NUMBER(1) NOT NULL,
	READCOUNT NUMBER(8) NOT NULL,
	
	CONSTRAINT "PK_REVIEW_SEQ" PRIMARY KEY ("REVIEW_SEQ"),
    CONSTRAINT "FK_REVIEW_ID" FOREIGN KEY("ID") REFERENCES MEMBER("ID")  
);

CREATE SEQUENCE SEQ_REVIEW
INCREMENT BY 1
START WITH 1;

SELECT COUNT(REVIEWBBS_SEQ) FROM REVIEWBBS