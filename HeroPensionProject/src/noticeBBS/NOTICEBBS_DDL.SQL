DROP TABLE NOTICEBBS CASCADE CONSTRAINTS;
DROP SEQUENCE SEQ_NOTICE;


CREATE TABLE NOTICEBBS(
	SEQ_NOTICE NUMBER(8),
	ID VARCHAR2(20) NOT NULL, 
	TITLE VARCHAR2(400) NOT NULL,
	CONTENT VARCHAR2(4000) NOT NULL,
	REF NUMBER(8) NOT NULL,
	STEP NUMBER(8) NOT NULL,
	DEPTH NUMBER(8) NOT NULL,
	WDATE DATE NOT NULL,
	DEL NUMBER(1) NOT NULL,
	READCOUNT NUMBER(8) NOT NULL,
	
	CONSTRAINT "PK_NOTICE_SEQ" PRIMARY KEY ("SEQ_NOTICE"),
    CONSTRAINT "FK_NOTICE_ID" FOREIGN KEY("ID") REFERENCES MEMBER("ID")  
);

CREATE SEQUENCE SEQ_NOTICE
INCREMENT BY 1
START WITH 1;

SELECT COUNT(SEQ_NOTICE) FROM NOTICEBBS